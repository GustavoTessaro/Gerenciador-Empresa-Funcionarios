    private void BT_Cadastrar1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BT_Cadastrar1MouseClicked
        String senha = String.valueOf(CampoDeTexto_Senha.getPassword());
        if(CampoDeTexto_Nome1.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null, "Por favor, digite o nome do usuario!");
        }
        else{
            if(senha.equalsIgnoreCase("")){
                JOptionPane.showMessageDialog(null, "Por favor, digite a senha!");
            }
            else{
                Main.Main.controller.CadastrarUsuario(CampoDeTexto_Nome1.getText(), senha);
            }
        }
    }//GEN-LAST:event_BT_Cadastrar1MouseClicked

    private void BT_LoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BT_LoginMouseClicked
        String senha = String.valueOf(CampoDeTexto_Senha.getPassword());
        boolean verifica = Main.Main.controller.BotaoLogar(CampoDeTexto_Nome1.getText(), senha);
        if(verifica == true){
            Main.Main.controller.MenuEmrpesas(CampoDeTexto_Nome1.getText(), senha);
        }
        else{
            JOptionPane.showMessageDialog(null, "Usuario ou senha inv√°lidas!");
        }
    }//GEN-LAST:event_BT_LoginMouseClicked


